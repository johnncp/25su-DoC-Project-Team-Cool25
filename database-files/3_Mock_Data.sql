USE euro_database;


-- ### AffinityResources
CREATE TABLE IF NOT EXISTS AffinityResources
(
    id      INT AUTO_INCREMENT PRIMARY KEY,
    resource_name VARCHAR(100),
    resource_type VARCHAR(50),
    focus_area VARCHAR(50),
    city VARCHAR(50),
    country_code   VARCHAR(10),
    description VARCHAR(200),
    website VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS Policies
(
    policy_id  INT PRIMARY KEY,
    policy_name VARCHAR(50),
    focus_area VARCHAR(50),
    country_code VARCHAR(10),
    year YEAR,
    description VARCHAR(200)
);

CREATE TABLE IF NOT EXISTS DeletedDaycareLocations (
    daycare_id    INT PRIMARY KEY,
    daycare_name  VARCHAR(100),
    opening_time  TIME,
    closing_time  TIME,
    monthly_price DECIMAL(7, 2),
    city          VARCHAR(50),
    country_code  VARCHAR(10)
);

CREATE TABLE IF NOT EXISTS DaycareData 
(
    id INT AUTO_INCREMENT PRIMARY KEY,
    daycare_id INT,
    enrollment INT, 
    year YEAR, 
    staff INT, 
    monthly_budget DECIMAL(9, 2),
    percent_budget_used DECIMAL(5,2),
    monthly_price DECIMAL(9,2),
    opening_time TIME, 
    closing_time TIME,
    FOREIGN KEY (daycare_id) REFERENCES DaycareLocations (daycare_id)
        ON UPDATE cascade ON DELETE restrict
);

